









void main () {

  int day = 4;

  switch (day) {
    case 1:
      print('일요일');
      break;

      case 2:
      print('월요일');
      break;

      case 3:
      print('화요일');
      break;

      case 4:
      print('수요일');
      break;

      case 5:
      print('목요일');
      break;

      case 6:
      print('금요일');
      break;

      case 7:
      print('토요일');
      break;
    default:
    print('잘못된 입력입니다.');
    break;
  }

}